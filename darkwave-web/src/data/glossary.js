export const categories = ['All', 'Crypto', 'Finance', 'DeFi', 'Technical Analysis', 'Crypto Slang']

export function getGlossaryTerm(term) {
  const key = Object.keys(glossary).find(k => k.toLowerCase() === term.toLowerCase())
  return key ? { term: key, ...glossary[key] } : null
}

export function searchGlossary(query) {
  if (!query) return Object.entries(glossary).map(([term, data]) => ({ term, ...data }))
  
  const lowerQuery = query.toLowerCase()
  return Object.entries(glossary)
    .filter(([term, data]) => 
      term.toLowerCase().includes(lowerQuery) ||
      data.plain.toLowerCase().includes(lowerQuery) ||
      data.smartass.toLowerCase().includes(lowerQuery)
    )
    .map(([term, data]) => ({ term, ...data }))
}

export function getRandomTerm() {
  const terms = Object.keys(glossary)
  const randomKey = terms[Math.floor(Math.random() * terms.length)]
  return { term: randomKey, ...glossary[randomKey] }
}
